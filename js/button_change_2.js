document.getElementById("expandMediaSensorFusion").addEventListener(
  "click",
  function(event) {
    if (event.target.value === "Show Media") {
      event.target.value = "Hide Media";
    } else {
      event.target.value = "Show Media";
    }
  },
  false
);

